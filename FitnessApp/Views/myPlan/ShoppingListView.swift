            VStack(spacing: 10) {
                Text("There are no items!")
                    .font(.title)
                    .fontWeight(.semibold)
                Text("Are you a productive person? I think you should click the add button and add a bunch of items to your todo list!")
                    .padding(.bottom, 20)
                
                NavigationLink (
                    destination: AddView().environmentObject(ListViewModel()),
                    label: {
                        Text("Up click Add ü•≥")
                            .foregroundColor(.white)
                            .font(.headline)
                            .frame(height: 55)
                            //.frame(maxWidth: .infinity)
                            .frame(maxWidth: 200)
                            //.background(Color.purple) // Pou≈æijeme kon≈°tantu Color.accentColor pre farbu tlaƒçidla
                            .background(Color(red: 0.7, green: 0.5, blue: 0.9))
                            .cornerRadius(10)
                    })
                    .padding(.horizontal, animate ? 30 : 50)
                    .shadow(
                        color: animate ? secondaryAccentColor.opacity(0.5) : Color.purple.opacity(0.7),
                        radius: animate ? 30: 10,
                        x: 0,
                        y: animate ? 35 : 25)
                    .scaleEffect(animate ? 1.1 : 1.0)
                    .offset(y: animate ? -7 : 0)
            }
            .frame(maxWidth: 380)
            .multilineTextAlignment(.center)
            .padding(40)
            .onAppear(perform: addAnimation)
        }
        .frame(maxWidth: .infinity, maxHeight: .infinity)
    }
    
    func addAnimation() {
        guard !animate else {return}
        DispatchQueue.main.asyncAfter(deadline: .now() + 1.5) {
            withAnimation(
                Animation
                    .easeOut(duration: 3.0)
                    .repeatForever()
            ) {
                animate.toggle()
            }
        }
    }
}

